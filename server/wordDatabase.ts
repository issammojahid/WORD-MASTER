// Build lookup sets for fast validation
const wordSets: Record<WordCategory, Set<string>> = {
  girl_names: new Set(girl_names),
  boy_names: new Set(boy_names),
  animals: new Set(animals),
  fruits: new Set(fruits),
  vegetables: new Set(vegetables),
  cities: new Set(cities),
  countries: new Set(countries),
  objects: new Set(objects),
};

export function validateWord(
  word: string,
  category: WordCategory,
  letter: string
): { valid: boolean; reason?: string } {
  if (!word || word.trim().length === 0) {
    return { valid: false, reason: "empty" };
  }

  const trimmed = word.trim();

  // Normalize: remove diacritics (harakat) for comparison
  const normalized = trimmed
    .replace(/[\u064B-\u065F]/g, "") // Remove harakat
    .replace(/[أإآ]/g, "أ"); // Normalize alef

  const normalizedLetter = letter
    .replace(/[\u064B-\u065F]/g, "")
    .replace(/[أإآ]/g, "أ");

  // Check if word starts with the correct letter
  if (!normalized.startsWith(normalizedLetter)) {
    return { valid: false, reason: "wrong_letter" };
  }

  // Check if word exists in the database
  const wordSet = wordSets[category];
  const found =
    wordSet.has(trimmed) ||
    wordSet.has(normalized) ||
    [...wordSet].some((w) => {
      const normW = w.replace(/[\u064B-\u065F]/g, "").replace(/[أإآ]/g, "أ");
      return normW === normalized;
    });

  if (!found) {
    return { valid: false, reason: "not_in_database" };
  }

  return { valid: true };
}

export function getWordsForLetter(
  category: WordCategory,
  letter: string
): string[] {
  const normalizedLetter = letter
    .replace(/[\u064B-\u065F]/g, "")
    .replace(/[أإآ]/g, "أ");

  const allWords =
    category === "girl_names"
      ? girl_names
      : category === "boy_names"
      ? boy_names
      : category === "animals"
      ? animals
      : category === "fruits"
      ? fruits
      : category === "vegetables"
      ? vegetables
      : category === "cities"
      ? cities
      : category === "countries"
      ? countries
      : objects;

  return allWords.filter((w) => {
    const norm = w
      .replace(/[\u064B-\u065F]/g, "")
      .replace(/[أإآ]/g, "أ");
    return norm.startsWith(normalizedLetter);
  });
}

